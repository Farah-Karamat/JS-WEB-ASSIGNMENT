//Q.2. Declare 5 legal & 5 illegal variable names.
//Answer:

// Legal Variable Names:

// 1. userName (Camel case is allowed)
// 2. total_price (Underscores are allowed)
// 3. $amount (Variable names can start with $)
// 4. _index (Variable names can start with an underscore)
// 5. user2 (Numbers can be included, but not at the start)

// Illegal Variable Names:

// 1. 2user (Cannot start with a number)
// 2. user-names (Hyphens are not allowed)
// 3. var (Cannot use reserved keywords)
// 4. user names (Spaces are not allowed)
// 5. @user (Special characters like @ are not allowed, except $ and _)

